#include <stdio.h>


int flag[6][12] = {
    {'#', '#', '#', '#', '#', '.', '.', '#', '#', '#', '#', '#'},
    {'#', '#', '#', '#', '#', '.', '.', '#', '#', '#', '#', '#'},
    {'.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'},
    {'.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'},
    {'#', '#', '#', '#', '#', '.', '.', '#', '#', '#', '#', '#'},
    {'#', '#', '#', '#', '#', '.', '.', '#', '#', '#', '#', '#'}
};

// flag[0][3] -> offset = 3
//      = flag address + 3

// flag[2][4] -> offset = (2 * 12) + 4
//      = flag address + (2 * 12) + 4

// flag[i][j]
// general formula
// offset = (i * N_COL) + j

// flag[5][4]
// offset = (5 * 12) + 4

int main(void) {
    for (int row = 0; row < 6; row++) {
        for (int col = 0; col < 12; col++) {
            char ch = flag[row][col];
            printf ("%c", ch);
        }
        printf ("\n");
    }
    return 0;
}